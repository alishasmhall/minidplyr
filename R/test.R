#' Add two numbers
#' 
#' Add two numbers supplied to the function.
#'
#' @param x A number to **add**.
#' @param y Another number to add.
#'
#' @return The sum of the two numbers or `x + y`.
#' @export 
#' 
#' @import ggplot2
#'
#' @examples
#' add(1, 2)
#' add(3:4, 5:6)
add <- function(x, y) {
  x + y
}

